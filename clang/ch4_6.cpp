#include <iostream>
using namespace std;

int main() {
    // 位运算直接操作二进制位
    // 先理解二进制：5 的二进制是 101，3 的二进制是 011

    // & 按位与：两个都是1，结果才是1
    cout << (5 & 3) << endl;  // 1 (101 & 011 = 001)
    //   101
    // & 011
    //   001 = 1

    // | 按位或：只要有一个是1，结果就是1
    cout << (5 | 3) << endl;  // 7 (101 | 011 = 111)
    //   101
    // | 011
    //   111 = 7

    // ^ 按位异或：相同为0，不同为1
    cout << (5 ^ 3) << endl;  // 6 (101 ^ 011 = 110)
    //   101
    // ^ 011
    //   110 = 6

    // ~ 按位取反：0变1，1变0
    cout << (~5) << endl;  // -6（涉及补码，了解即可）

    // << 左移：二进制位整体左移，右边补0
    // 左移 n 位相当于乘以 2^n
    cout << (3 << 2) << endl;  // 12 (3 × 2² = 12)
    // 3 = 011，左移2位 = 01100 = 12

    // >> 右移：二进制位整体右移，左边补符号位
    // 右移 n 位相当于除以 2^n（向下取整）
    cout << (12 >> 2) << endl;  // 3 (12 ÷ 2² = 3)

    // 位运算的常见用途：

    // 1. 判断奇偶（比 %2 更快）
    // n & 1 == 1 表示奇数，n & 1 == 0 表示偶数
    int n = 7;
    if (n & 1) cout << n << " 是奇数" << endl;

    // 2. 乘以/除以 2 的幂
    int x = 10;
    cout << (x << 1) << endl;  // 20 (× 2)
    cout << (x << 3) << endl;  // 80 (× 8)
    cout << (x >> 1) << endl;  // 5  (÷ 2)

    // 3. 交换两个变量（不用临时变量）
    int a = 3, b = 5;
    a = a ^ b;
    b = a ^ b;
    a = a ^ b;
    cout << "a=" << a << ", b=" << b << endl;  // a=5, b=3

    // 4. 取最低位的 1：n & (-n)
    // 5. 把最低位的 1 变成 0：n & (n - 1)

    return 0;
}
