#include <iostream>  // 头文件：包含输入输出功能
using namespace std; // 使用标准名字空间，这样可以直接写 cout 而不是 std::cout

int main()
{                                    // main 是程序入口，每个程序必须有且只有一个 main 函数
    cout << "Hello, World!" << endl; // cout 是输出，<< 是输出运算符，endl 换行
    return 0;                        // 返回 0 表示程序正常结束
}
