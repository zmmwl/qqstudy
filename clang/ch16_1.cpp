#include <iostream>
using namespace std;

// 例1：百钱买百鸡
// 公鸡5元一只，母鸡3元一只，小鸡1元3只
// 100元买100只鸡，公鸡、母鸡、小鸡各多少只？
int main() {
    // 公鸡最多买 100/5=20 只
    for (int x = 0; x <= 20; x++) {          // x 是公鸡数量
        // 母鸡最多买 (100-5*x)/3 只
        for (int y = 0; y <= 33; y++) {       // y 是母鸡数量
            int z = 100 - x - y;              // z 是小鸡数量
            // 检查：总钱数是否为100，且小鸡数量是3的倍数
            if (5 * x + 3 * y + z / 3 == 100 && z % 3 == 0) {
                cout << "公鸡:" << x << " 母鸡:" << y << " 小鸡:" << z << endl;
            }
        }
    }
    return 0;
}
